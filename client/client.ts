const addRack = (
  powerDesign: number,
  powerUsed: number,
  rackName: string,
) => {
  let message = '';
  let newline = document.createElement('br');
  const rackDiv = document.getElementById('racks');
  if (rackDiv !== null) {
    // breaks
    rackDiv.appendChild(newline);
    newline = document.createElement('br');
    rackDiv.appendChild(newline);
    // rack
    const newDiv = document.createElement('div');
    rackDiv.appendChild(newDiv);
    message += `Rack - ${rackName}\n`;
    message += `Power design - ${powerDesign}\n`;
    message += `Power Used - ${powerUsed.toFixed(2)}\n`;
    message += `Percentage - ${((powerUsed / powerDesign) * 100).toFixed(2)}\n`;
    newDiv.innerText = message;
    newDiv.classList.add('rackDiv');
    // design bar
    const powerDesignDiv = document.createElement('div');
    newDiv.appendChild(powerDesignDiv);
    powerDesignDiv.classList.add('powerDesign');
    powerDesignDiv.style.width = `${powerDesign * 10}px`;
    // usage bar
    const powerUsageDiv = document.createElement('div');
    newDiv.appendChild(powerUsageDiv);
    powerUsageDiv.classList.add('powerUsed');
    powerUsageDiv.style.width = `${powerUsed * 10}px`;

  }
};
